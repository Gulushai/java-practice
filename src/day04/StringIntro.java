package day04;

public class StringIntro {

	public static void main(String[] args) {
		
		String food;
		food = "ice cream";
		System.out.println(food);
		
		String job = "SDET";
		String company = "Google";
		
		System.out.println("I work as " + job + " in " + company);
		
		String city1 = "Tashkent";
		String city2 = "Kyiv";
		String city3 = "Bishkek";
		
		System.out.println("from " + city1 + " to " + city2 + " is $500");
		System.out.println("from " + city2 + " to " + city3 + " is $700");
		System.out.println("I have been to " + city1 +", " + city2 + ", "+ city3 + ".");

	}

}
