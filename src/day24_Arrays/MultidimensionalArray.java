package day24_Arrays;

public class MultidimensionalArray {

	public static void main(String[] args) {
		
		int[][] ducks = {{1, 2, 3, 4}, {1, 2, 3, 4, 5}, {1, 2, 3}};
		System.out.println(ducks.length);
		System.out.println(ducks[1].length);
		System.out.println(ducks[0][4]);
		System.out.println(ducks[2][2]);
		
		int nums[][] = new int[5][3];
	}

}
